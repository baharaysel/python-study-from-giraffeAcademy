def say_hi(name):
	print("Hello " + name)

say_hi("Furkan")
say_hi("Aysel")


def say_hii(number):
	print(number*3)

say_hii(5)

def say_hi(name, age):
	print("Hello "+ name + ", you are " + str(age))
say_hi("Aysel", 36)
say_hi("Handsome", 36)

